// Group by province and sort resorts alphabetically within each province
export const RESORTS_BY_PROVINCE = SKI_RESORTS.reduce((acc, r) => {
  if (!acc[r.province]) acc[r.province] = [];
  acc[r.province].push(r);
  return acc;
}, {});

Object.keys(RESORTS_BY_PROVINCE).forEach(province => {
  RESORTS_BY_PROVINCE[province].sort((a, b) => a.name.localeCompare(b.name));
});

